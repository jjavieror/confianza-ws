package co.com.ws.confianza.model.dto.consorciotemporales;

public class ConsorcioTemporalesDTO {
	public String razonSocialIntermediario;
	public String NombreConsorcioUnionTemporal;
	public String Documento;
	public String valorCupo;
	public String valorCumulo;
	public String valorDisponible;

	public String getRazonSocialIntermediario() {
		return razonSocialIntermediario;
	}

	public void setRazonSocialIntermediario(String razonSocialIntermediario) {
		this.razonSocialIntermediario = razonSocialIntermediario;
	}

	public String getNombreConsorcioUnionTemporal() {
		return NombreConsorcioUnionTemporal;
	}

	public void setNombreConsorcioUnionTemporal(String nombreConsorcioUnionTemporal) {
		NombreConsorcioUnionTemporal = nombreConsorcioUnionTemporal;
	}

	public String getDocumento() {
		return Documento;
	}

	public void setDocumento(String documento) {
		Documento = documento;
	}

	public String getValorCupo() {
		return valorCupo;
	}

	public void setValorCupo(String valorCupo) {
		this.valorCupo = valorCupo;
	}

	public String getValorCumulo() {
		return valorCumulo;
	}

	public void setValorCumulo(String valorCumulo) {
		this.valorCumulo = valorCumulo;
	}

	public String getValorDisponible() {
		return valorDisponible;
	}

	public void setValorDisponible(String valorDisponible) {
		this.valorDisponible = valorDisponible;
	}

}
