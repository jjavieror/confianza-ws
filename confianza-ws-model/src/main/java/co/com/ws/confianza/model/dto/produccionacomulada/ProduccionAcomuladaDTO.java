package co.com.ws.confianza.model.dto.produccionacomulada;

public class ProduccionAcomuladaDTO {
	public String nit;
	public String nombre;
	public String periodo;
	public String cumplimiento;
	public String rcEctractual;
	public String rcGeneral;
	public String trcm;
	public String property;
	public String transporte;

	public String getNit() {
		return nit;
	}

	public void setNit(String nit) {
		this.nit = nit;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getPeriodo() {
		return periodo;
	}

	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}

	public String getCumplimiento() {
		return cumplimiento;
	}

	public void setCumplimiento(String cumplimiento) {
		this.cumplimiento = cumplimiento;
	}

	public String getRcEctractual() {
		return rcEctractual;
	}

	public void setRcEctractual(String rcEctractual) {
		this.rcEctractual = rcEctractual;
	}

	public String getRcGeneral() {
		return rcGeneral;
	}

	public void setRcGeneral(String rcGeneral) {
		this.rcGeneral = rcGeneral;
	}

	public String getTrcm() {
		return trcm;
	}

	public void setTrcm(String trcm) {
		this.trcm = trcm;
	}

	public String getProperty() {
		return property;
	}

	public void setProperty(String property) {
		this.property = property;
	}

	public String getTransporte() {
		return transporte;
	}

	public void setTransporte(String transporte) {
		this.transporte = transporte;
	}

}
